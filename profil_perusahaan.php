<p style="font-size:18px;">
Perusahaan Air Bersih Kota Palembang di dirikan pada tahun 1929 oleh pemerintah Kolonial Belanda yang berlokasi di 3 ilir Palembang dengan nama Palembang Water Leading. Pendirian instalasi I selesai pada tahun 1933, setelah Indonesia merdeka perusahaan diambil alih oleh kota madya Palembang Seksi Teknik Air Bersih Dinas Pekerjaan Umum kota madya Palembang. Berdasarkan surat keputusan Walikota Madya Palembang pada tanggal 21 Agustus 1963 perusahaan Air Bersih tersebut menjadi perusahaan Air Bersih yang melaksanakan produksi dan administrasi. Pada tahun 1976 statusnya berubah menjadi Perusahaan Daerah Air Minum (PDAM) Tirta Musi berdasarkan Perda Kota madya Daerah Tingkat II Palembang Nomor: 1/Perda/Huk/1976 tanggal 3 April 1976 dan Surat Keputusan Gubernur Kepala Daerah Tingkat I Sumatera Selatan Nomor: 20/KPTS/IV/1976 tanggal 11 Juni 1976.
</p>

<h1 style="text-align:center;">Visi</h1>
<p style="text-align:center;font-size:20px;">Menjadi perusahaan smart happy yang unggul dalam penyediaan air minum dan pengelola air limbah di Indonesia pada tahun 2028</p>



<h1 style="text-align:center;">Misi</h1>

<ol style="font-size:18px;">
<li>Menjadi penyedia air minum yang handal berprinsip pada pelayanan 4K (kualitas, kuantitas, kontinuitas dan keterjangkauan) serta GCG (Good Corporate Governance)</li>

<li>Mengintegrasikan semua informasi produksi, distribusi, pelayanan dan sumber daya dalam pengembangan transformasi teknologi digital sebagai sumber kekuatan perusahaan</li>

<li>Mangutamakan kepuasan/kebahagiaan pelanggan dengan pelayanan yang lancer, aman, cukup, teratur dan bertanggung jawab sehingga menjadi kebanggaan masyarakat dan pemerintah</li>

<li>Mampu memberikan kesejahteraan dan kebahagiaan terbaik secara berkelanjutan bagi karyawan dan menjadi tempat memperluas wawasan pengetahuan dan keterampilan tentang penyediaan air minum dalam upaya pengembangan diri yang lebih kreatif dan inovatif dengan teknologi tepat guna, efisien dan terintegrasi, berbasis sumber daya dan kearifan lokal</li>

</ol>


